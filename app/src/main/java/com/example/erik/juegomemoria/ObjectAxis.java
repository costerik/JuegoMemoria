public class ObjectAxis {
    float x;
    float y;
    float z;

    public ObjectAxis(float x,float y,float z){
        this.x=x;
        this.y=y;
        this.z=z;
    }

    public float getX(){
        return this.x;
    }

    public float getY(){
        return this.y;
    }
    public float getZ(){
        return this.z;
    }
}
